export interface PromptParts {
  topic: string;
  contextText: string;   // טקסט ההקשר של השחקן (פרופיל + נתונים + זיכרון)
}

export function buildSystemPrompt({ topic, contextText }: PromptParts): string {
  const topicLabel = TOPIC_LABELS[topic] ?? "שאלה חופשית";
  const knowledge = knowledgeForTopic(topic);
  const sections = [
    BASE_SYSTEM,
    `הנושא הנוכחי של השיחה: ${topicLabel}.`,
    knowledge ? `ידע מדעי רלוונטי לנושא (להסתמך עליו):\n${knowledge}` : "",
    contextText ? `הקשר אישי על השחקן (השתמש בו כדי להתאים את התשובה):\n${contextText}` : "אין עדיין מספיק נתונים אישיים – ענה לפי עקרונות כלליים וציין שתלמד יותר עם הזמן.",
  ].filter(Boolean);
  return sections.join("\n\n");
}

