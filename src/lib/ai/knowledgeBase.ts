// טקסט ידע להזרקה לפרומפט לפי נושא
export function knowledgeForTopic(topic: string): string {
  const cards = KNOWLEDGE_CARDS[topic] ?? [];
  if (!cards.length) return "";
  return cards.map((c) =>
    `• ${c.topic}: ${c.principle} המלצה בטוחה: ${c.recommendation}` +
    (c.referral && c.referral !== "—" ? ` הפניה: ${c.referral}` : "")
  ).join("\n");
}
