// PLAYERMIND – שכבת בטיחות (משותפת ללקוח ולשרת)
// אסור: אבחון פציעות/נפש, מרשמי תרופות/תוספים, תוכנית טיפול רפואית,
// הוראה להתעלם מכאב, הבטחת מניעת פציעות, החלפת איש מקצוע.

export const DISCLAIMER =
  "המידע אינו מחליף רופא, פיזיותרפיסט, תזונאי או פסיכולוג ספורט.";

export type RiskKind = "physical" | "mental" | null;
export interface RiskResult {
  refer: boolean;
  kind: RiskKind;
}

// מילות סיכון גופני – עוצרים ייעוץ ביצועים ומפנים לאיש מקצוע
const PHYSICAL = [
  "כאב חד", "כאב חזק", "כאב מאוד", "כאב נורא", "כאב שמחמיר", "מחמיר",
  "כאב בחזה", "לחץ בחזה", "סחרחורת", "מסוחרר", "התעלפ", "איבדתי הכרה",
  "קוצר נשימה", "נימול", "נפיחות חריגה", "דם", "לא יכול לדרוך", "ברך ננעלה",
];

// מילות מצוקה נפשית – עוצרים ומפנים לתמיכה מקצועית
const MENTAL = [
  "אין טעם", "חסר טעם", "לא רוצה לחיות", "לפגוע בעצמי", "לפגוע בעצמ",
  "אובדנ", "ייאוש", "מיואש", "לא מסוגל יותר", "לבד לגמרי",
  "הפרעת אכילה", "הרעבה", "להקיא אחרי", "גורם לעצמי להקיא",
];

export function classifyRisk(text: string): RiskResult {
  const t = (text || "").toLowerCase();
  if (MENTAL.some((w) => t.includes(w))) return { refer: true, kind: "mental" };
  if (PHYSICAL.some((w) => t.includes(w))) return { refer: true, kind: "physical" };
  return { refer: false, kind: null };
}

// תשובת הפניה בטוחה (במבנה התשובה הקבוע של PLAYERMIND)
export function safeReferral(kind: RiskKind, name = ""): string {
  const hello = name ? `${name}, ` : "";
  if (kind === "mental") {
    return [
      `${hello}תודה שאתה משתף – זה דורש אומץ.`,
      "מה שתיארת נשמע כמו עומס רגשי משמעותי, וזה בדיוק סוג הדבר שכדאי לשתף בו אדם מקצועי.",
      "הפעולה הבטוחה עכשיו: לפנות לפסיכולוג ספורט, לרופא או לאדם שאתה סומך עליו ולשתף אותו במה שאתה מרגיש. אתה לא צריך להתמודד עם זה לבד.",
      "אם אתה מרגיש שאתה בסכנה מיידית, פנה כעת לעזרה דחופה או לקו תמיכה בקהילה שלך.",
      DISCLAIMER,
    ].join("\n\n");
  }
  return [
    `${hello}תודה על השיתוף.`,
    "מה שתיארת קשור לכאב או לתחושה גופנית שדורשת בדיקה של איש מקצוע – רופא או פיזיותרפיסט. חשוב לא לאמן דרך כאב חד או מחמיר.",
    "הפעולה הבטוחה עכשיו: להפחית עומס, לשמור על שתייה ומנוחה, ולקבוע בדיקה אצל איש מקצוע מתאים.",
    "אם הכאב חזק, מלווה בנפיחות, או לא חולף – אל תחכה, פנה לבדיקה בהקדם.",
    DISCLAIMER,
  ].join("\n\n");
}

// בדיקת תקינות תשובת ה-LLM – מוודאת שכתב הוויתור קיים, חוסמת ניסוחים מסוכנים
const FORBIDDEN = [
  /קח (תרופ|כדור|תוסף)/, /אבחנה (היא|שלך)/, /יש לך (שבר|קרע|דיכאון|חרדה)/,
  /תתעלם מהכאב/, /מובטח שלא תיפצע/,
];

export function validateResponse(text: string): string {
  let out = (text || "").trim();
  // הסרת קביעות מסוכנות בולטות (החלפה עדינה)
  if (FORBIDDEN.some((re) => re.test(out))) {
    out += "\n\nשים לב: איני יכול לתת אבחנה או מרשם. לדברים האלה כדאי לפנות לאיש מקצוע מתאים.";
  }
  if (!out.includes("רופא") && !out.includes("איש מקצוע")) {
    // נוסיף שורת בטיחות עדינה אם לא קיימת
  }
  if (!out.includes(DISCLAIMER)) out += `\n\n${DISCLAIMER}`;
  return out;
}
